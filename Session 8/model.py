import torch.nn as nn
import torchsummary as summary 


class ConvLayer(nn.Module):
    def __init__(self, in_channels, out_channels, padding=1, bias=False, skip=False, norm_type=None, n_groups=4, dropout=0):
        super(ConvLayer, self).__init__()

        self.skip = skip
        self.norm_type = norm_type
        self.n_groups = n_groups

        self.convlayer = nn.Conv2d(in_channels, out_channels, 3, padding=padding, bias=bias, padding_mode='replicate')
        self.normlayer = None
        if self.norm_type is not None:
            self.normlayer = self.get_norm_layer(out_channels)
        self.activationLayer = nn.ReLU()
        self.dropoutlayer = None
        if dropout > 0:
            self.dropoutlayer = nn.Dropout(dropout)

    def get_norm_layer(self, c):
        if self.norm_type == 'batch':
            return nn.BatchNorm2d(c)
        elif self.norm_type == 'layer':
            return nn.GroupNorm(1, c)
        elif self.norm_type == 'group':
            return nn.GroupNorm(self.n_groups, c)
        else:
            raise Exception(f'Unknown norm_type: {self.norm_type}')

    def forward(self, x):
        Y = x
        x = self.convlayer(x)
        if self.normlayer is not None:
            x = self.normlayer(x)
        if self.skip:
            x += Y
        x = self.activationLayer(x)
        if self.dropoutlayer is not None:
            x = self.dropoutlayer(x)
        return x


class Model(nn.Module):
    def __init__(self, norm_type=None, n_groups=4, dropout=0, skip=False):
        super(Model, self).__init__()

        self.norm_type = norm_type
        self.n_groups = n_groups
        self.dropout = dropout

        self.conv1 = self.make_conv_block(3, 16, size=2, padding=0, skip=False)
        self.trans1 = self.make_trans_block(16, 24)
        self.conv2 = self.make_conv_block(24, 24, size=3, padding=1, skip=skip)
        self.trans2 = self.make_trans_block(24, 32)
        self.conv3 = self.make_conv_block(32, 32, size=3, padding=1, skip=skip)

        self.output = nn.Sequential(
            nn.AdaptiveAvgPool2d(1),
            nn.Conv2d(32, 10, 1, bias=True),
            nn.Flatten(),
            nn.LogSoftmax(-1)
        )

    def make_conv_block(self, in_channels, out_channels, size=1, padding=1, bias=False, skip=False):
        layers = list()
        for i in range(0, size):
            layers.append(
                ConvLayer(out_channels if i > 0 else in_channels, out_channels, padding=padding, bias=bias, skip=skip,
                          norm_type=self.norm_type, n_groups=self.n_groups, dropout=self.dropout)
            )
        return nn.Sequential(*layers)

    def make_trans_block(self,in_channels, out_channels):
        return nn.Sequential(
            nn.Conv2d(in_channels, out_channels, 1, bias=False),
            nn.MaxPool2d(2, 2)
        )

    def forward(self, x):
        x = self.conv1(x)
        x = self.trans1(x)
        x = self.conv2(x)
        x = self.trans2(x)
        x = self.conv3(x)
        x = self.output(x)
        return x



